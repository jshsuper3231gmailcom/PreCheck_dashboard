CREATE SEQUENCE SEQ_ANALYZE_RESULT START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE SEQ_ANALYZE_HISTORY START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE SEQ_COLLECT_HISTORY START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE SEQ_COLLECT_LOG START WITH 1 INCREMENT BY 1;

CREATE TABLE TB_ANALYZE_RESULT (
    ANALYZE_RESULT_ID   NUMERIC(19,0)   NOT NULL,
    COLLECT_LOG_ID      NUMERIC(19,0),
    SERVER_ID           VARCHAR(100)    NOT NULL,
    SERVER_IP           VARCHAR(15)     NOT NULL,
    LOG_TYPE            VARCHAR(10)     NOT NULL,
    LOG_ID              VARCHAR(30)     NOT NULL,
    LOG_TIMESTAMP       TIMESTAMP       NOT NULL,
    LOG_CONTENT         VARCHAR(4000)   NOT NULL,
    LOG_VALUE           NUMERIC(18,6),
    ANALYZE_LEVEL       VARCHAR(10)     NOT NULL,
    ANALYZE_MESSAGE     VARCHAR(2000)   NOT NULL,
    THRESHOLD_VALUE     NUMERIC(18,6),
    THRESHOLD_OPERATOR  VARCHAR(5),
    WARNING_RATIO       NUMERIC(5,2),
    NOTIFY_YN           CHAR(1)         NOT NULL,
    NOTIFY_AT           TIMESTAMP,
    ANALYZE_DATE        VARCHAR(8)      NOT NULL,
    ANALYZE_DATETIME    TIMESTAMP       NOT NULL,
    COLLECT_DATE        VARCHAR(8)      NOT NULL,
    CREATED_AT          TIMESTAMP       NOT NULL,
    CONSTRAINT PK_ANALYZE_RESULT PRIMARY KEY (ANALYZE_RESULT_ID)
);

CREATE TABLE TB_ANALYZE_HISTORY (
    ANALYZE_HISTORY_ID   NUMERIC(19,0)  NOT NULL,
    SERVER_ID            VARCHAR(100)   NOT NULL,
    SOURCE_FILE_PATH     VARCHAR(500),
    ANALYZE_STATUS       VARCHAR(10)    NOT NULL,
    LAST_ANALYZE_LOG_ID  NUMERIC(19,0),
    TOTAL_COUNT          NUMERIC(10,0),
    SUCCESS_COUNT        NUMERIC(10,0),
    FAIL_COUNT           NUMERIC(10,0),
    ERROR_COUNT          NUMERIC(10,0),
    WARNING_COUNT        NUMERIC(10,0),
    FAIL_REASON          VARCHAR(1000),
    ANALYZE_START_AT     TIMESTAMP      NOT NULL,
    ANALYZE_END_AT       TIMESTAMP,
    ANALYZE_DATE         VARCHAR(8)     NOT NULL,
    CREATED_AT           TIMESTAMP      NOT NULL,
    UPDATED_AT           TIMESTAMP,
    CONSTRAINT PK_ANALYZE_HISTORY PRIMARY KEY (ANALYZE_HISTORY_ID)
);

CREATE TABLE TB_COLLECT_HISTORY (
    COLLECT_HISTORY_ID   NUMERIC(19,0)  NOT NULL,
    SERVER_ID            VARCHAR(100)   NOT NULL,
    SOURCE_FILE_PATH     VARCHAR(500)   NOT NULL,
    COLLECT_STATUS       VARCHAR(10)    NOT NULL,
    COLLECTED_COUNT      NUMERIC(10,0),
    RETRY_COUNT          NUMERIC(5,0),
    FAIL_REASON          VARCHAR(1000),
    COLLECT_START_AT     TIMESTAMP      NOT NULL,
    COLLECT_END_AT       TIMESTAMP,
    COLLECT_DATE         VARCHAR(8)     NOT NULL,
    CREATED_AT           TIMESTAMP      NOT NULL,
    CONSTRAINT PK_COLLECT_HISTORY PRIMARY KEY (COLLECT_HISTORY_ID)
);

CREATE TABLE TB_COLLECT_LOG (
    COLLECT_LOG_ID       NUMERIC(19,0)  NOT NULL,
    SERVER_ID            VARCHAR(100)   NOT NULL,
    SERVER_IP            VARCHAR(15)    NOT NULL,
    LOG_TYPE             VARCHAR(10)    NOT NULL,
    LOG_ID               VARCHAR(30)    NOT NULL,
    LOG_TIMESTAMP        TIMESTAMP      NOT NULL,
    LOG_CONTENT          VARCHAR(4000)  NOT NULL,
    LOG_VALUE            NUMERIC(18,6),
    RAW_LOG              VARCHAR(4000)  NOT NULL,
    SOURCE_FILE_PATH     VARCHAR(500)   NOT NULL,
    COLLECT_DATETIME     TIMESTAMP      NOT NULL,
    COLLECT_DATE         VARCHAR(8)     NOT NULL,
    CREATED_AT           TIMESTAMP      NOT NULL,
    CONSTRAINT PK_COLLECT_LOG PRIMARY KEY (COLLECT_LOG_ID)
);

